<template>
    <div>
        Users ({{numUsers}}):
        <select>
            <option :value="user.id" v-for="user in users">
                {{user.name}} - {{user.email}}
            </option>
        </select>

    </div>
</template>

<style>

</style>

<script>

    import axios from 'axios'
  export default {
    name: 'name',
    data () {
      return {
        users: [],
      }
    },
    computed: {
      numUsers() {
        return this.users.length
      }

  },

    mounted () {
      console.log('Mounted ok')


      //Les promises sÃ³n codi asincron.

      axios.get('api/v1/users').then( (response)=>{
            this.users = response.data
        }

      ).catch( error => {

        console.log(error)

      }).then()


    }
  }
</script>